    public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) jtUsuario.getModel();
        modelo.setNumRows(0);
        UsuarioDAO udao = new UsuarioDAO();
        
        for(Usuario u: udao.read()){
            //DEIXAR A COLUNA DO TIPO OBJECT
            modelo.addRow(new Object[]{
                u.getIdusuario(),
                u.getNome(),
                u.getSobrenome(),
                u.getEmail(),
                u.getLogin(),
                u.getDataNasc(),
                u.getStatus()
            });
            
        }
    }
    
    public void readJTableForName(String nome){
        DefaultTableModel modelo = (DefaultTableModel) jtUsuario.getModel();
        modelo.setNumRows(0);
        UsuarioDAO udao = new UsuarioDAO();
        
        for(Usuario u: udao.readForName(nome)){
            //DEIXAR A COLUNA DO TIPO OBJECT
            modelo.addRow(new Object[]{
                u.getIdusuario(),
                u.getNome(),
                u.getSobrenome(),
                u.getEmail(),
                u.getLogin(),
                u.getDataNasc(),
                u.getStatus()
            });
            
        }
    }

