    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Usuario c = new Usuario();
        UsuarioDAO dao = new UsuarioDAO();   
        if(jtUsuario.getSelectedRow() != -1){
            int i = JOptionPane.showConfirmDialog(null ,"Tem certeza que deseja inativar este usuário?", "Inativar usuário",JOptionPane.YES_NO_OPTION);
            if(i == JOptionPane.YES_OPTION){ 
                c.setIdusuario((int)jtUsuario.getValueAt(jtUsuario.getSelectedRow(), 0));
                if(!dao.verificarStatusS((String)jtUsuario.getValueAt(jtUsuario.getSelectedRow(), 4))){
                    dao.inativar(c);
                    readJTable();
                }else{
                    JOptionPane.showMessageDialog(null, "Usuario encontra-se inativado", "Atenção", JOptionPane.ERROR_MESSAGE);
                }
            }else{
                
            }
        }else{
            JOptionPane.showMessageDialog(null, "Selecione uma linha cadastrada", "Atenção", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

